// F12 - Consoleで確認
console.log("Hello World!!"); // = Hello World!!
console.log("Hello " + "World!!"); // = Hello World!!
console.log(65536); // = 65536
console.log(10 + 5); // = 15
console.log(10 * 5); // = 50
// 割り算の余りの値を求めたい場合は「%」を使用します。
console.log(10 % 2); // = 0
/*
上記の場合、10割る2で余り0
そのため出力は「0」
下記の場合、出力は「1」になります
*/
console.log(10 % 3); // = 1
console.log("10 + 8"); // = 10 + 8
console.log("10 + 8 = " + (10 + 8)); // = 18
console.log("20 % 3 の余リは" + (20 % 3)); // = 10 % 3 の余リは2

